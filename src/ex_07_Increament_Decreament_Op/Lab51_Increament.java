package ex_07_Increament_Decreament_Op;

public class Lab51_Increament
{
    public static void main(String[] args) {
        // Pre Increament --> ++ Operand
        // Value is increament first then it stored in the result
        // Value is increased by 1
        int a =10;
        int b = ++a; // 10+1 -> b -> 11
        System.out.println(b); //11
        System.out.println(a); // 11


    }
}
