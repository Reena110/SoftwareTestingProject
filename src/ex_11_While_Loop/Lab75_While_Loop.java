package ex_11_While_Loop;

public class Lab75_While_Loop
{
    public static void main(String[] args) {
        int i =0; //Intilizaton
        while(i<10) //condition must be true to enter the loop
        {
            System.out.println("Start the Program");
            System.out.println(i);
            i++ ; //Updation
        }
    }
}
